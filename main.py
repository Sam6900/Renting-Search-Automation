soup = BeautifulSoup(zillow_data, "html.parser")
prices_data = soup.select(".hRqIYX span")
prices = []
for price in prices_data:
    price = price.get_text()
    if "/" in price:
        price = price.split("/")[0]
    elif "+" in price:
        price = price.split("+")[0]
    prices.append(price)

addresses = [addr.get_text() for addr in soup.select("a address")]
